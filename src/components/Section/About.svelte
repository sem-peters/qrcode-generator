        <h2>What is the QR Code Generator?</h2>
        <p>Sem Peters' QR Code Generator is a <strong>free</strong>, <strong>simple</strong> and
            <strong><a href="https://en.wikipedia.org/wiki/Open_source" target="_blank">open-source</a></strong>
            application that allows you to create and save QR codes for free.
        </p>

        <h3>How does it work?</h3>
        <p>Just enter any text value, for example a URL, into the 'QR Code Value' field. A regular QR Code has a
            black foreground and a white background. The QR Code Generator allows you to change these colors, including
            to transparency. You can check if the QR Code is scannable in the preview. If you're happy with your
            creation, select your preferred file format (note that JPEG does not support transparency) and click the
            'Download' button.
        </p>

        <h3>Where are the QR Codes saved?</h3>
        <p>
            The QR Codes are saved in your browser on your machine. The app has no database where the QR Codes are
            stored. This allows me to keep this service free, as there are essentially no costs involved.
            <br/>
            Saving QR Codes to your machine is disabled by default and opt-in.
        </p>

